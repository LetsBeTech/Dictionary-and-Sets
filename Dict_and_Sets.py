#Syntax of dictionary

MyDict = {
"run":"in a quick manner",
"immortal":"A person who can't die"
}

#This will return 'in a quick manner'
print(MyDict["run"])

#This will return "A person who can't die"
print(MyDict["immortal"])

#prints all the keys present in MyDict
print(MyDict.keys())

#Prints all the values present in MyDict
print(MyDict.values())

